numberInRange = int(input("Please enter a number: "))

if 21 <= numberInRange <= 29:
    print("Your value is within range.")
elif numberInRange < 21:
    print("Your value is below the range.")
elif numberInRange > 29:
    print("Your value is above the range.")
    
